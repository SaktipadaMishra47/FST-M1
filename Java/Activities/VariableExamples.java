package Day1;

public class VariableExamples {

	static int a = 20, b = 20;

	public static void main(String args[]) {
		
		int c = a + b;
		int d = a/b;
		int e = a*b;
		int f = a%b;
		
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}
}
